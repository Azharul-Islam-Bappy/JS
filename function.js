// All about  JS function 

// function declaration and named function
// when we declare function using the "function" keyword it is called declared function

// example:
function name() {
  // do something 
}

// function expression 
// when we assign a function to a variable it is called function expression 

// example: 
let greet = function() {
  // greet the person 
}

// anonymous function
// anonymous functions are functions that isn't named 

// example: 
function () {
  // this is a anonymous function
}

// Arrow function: they are also anonymous function
// syntax:  (parameters here) => {} 
let arrowFunc = () => {
  // arrow function
}

